use crate::output::{bold, dim, green, red, yellow};
use crate::plugin::builtin::health_config::{self, HealthConfig};
use serde::Serialize;
use std::path::Path;
use std::process::Command;

#[derive(Debug, Serialize)]
struct HealthReport {
    passed: u32,
    warnings: u32,
    errors: u32,
    sections: Vec<HealthSection>,
}

#[derive(Debug, Serialize)]
struct HealthSection {
    name: String,
    checks: Vec<HealthCheckRecord>,
}

#[derive(Debug, Serialize)]
struct HealthCheckRecord {
    name: String,
    status: String,
    summary: String,
    details: Vec<String>,
}

// ── Public entry point ──────────────────────────────────────────────

pub fn run(repo_root: &Path, args: &[&str]) {
    let subcommand = args.first().copied().filter(|a| !a.starts_with('-'));
    let json = args.contains(&"--json");

    if args.iter().any(|a| *a == "--help" || *a == "-h") {
        if subcommand.is_none() {
            print_help();
        }
        return;
    }

    match subcommand {
        Some("init") => cmd_init(repo_root),
        Some("export") => cmd_export(repo_root),
        Some(other) => {
            eprintln!("Unknown health subcommand: {other}");
            eprintln!("Run `repo health --help` for usage.");
        }
        None => {
            let verbose = args.iter().any(|a| *a == "--verbose" || *a == "-v");
            let check_updates = args.iter().any(|a| *a == "--check-updates" || *a == "-u");
            cmd_check(repo_root, verbose, check_updates, json);
        }
    }
}

// ── init: write blank template ──────────────────────────────────────

fn cmd_init(repo_root: &Path) {
    let path = repo_root.join(".repo").join("health.toml");
    if path.exists() {
        eprintln!("  {} .repo/health.toml already exists", yellow("!!"));
        eprintln!("  Use `repo health export` to overwrite with current state,");
        eprintln!("  or edit the file directly.");
        return;
    }

    let template = health_config::blank_template();
    if let Err(e) = std::fs::write(&path, template) {
        eprintln!("Error writing {}: {e}", path.display());
        std::process::exit(1);
    }

    println!("  {} wrote .repo/health.toml", green("ok"));
    println!("  Edit the file to specify required tools and versions.");
}

// ── export: snapshot current env ────────────────────────────────────

fn cmd_export(repo_root: &Path) {
    let checks = default_checks();
    let mut found: Vec<(String, Option<String>)> = Vec::new();

    for check in &checks {
        let result = check.execute();
        if let CheckResult::Ok(version) = result {
            found.push((check.name.clone(), Some(version)));
        }
    }

    let cage = cage_name(&detect_cage());
    let config = health_config::snapshot_current(&found, cage);
    let toml_str = config.to_toml();

    let path = repo_root.join(".repo").join("health.toml");

    // Add a header comment.
    let content = format!(
        "# .repo/health.toml — Exported from current environment\n\
         # Generated by `repo health export`\n\
         # Edit as needed, then run `repo health` to validate.\n\n\
         {toml_str}"
    );

    if let Err(e) = std::fs::write(&path, &content) {
        eprintln!("Error writing {}: {e}", path.display());
        std::process::exit(1);
    }

    println!("  {} wrote .repo/health.toml", green("ok"));
    println!();
    print!("{content}");
}

// ── check: main health check ────────────────────────────────────────

fn cmd_check(repo_root: &Path, verbose: bool, check_updates: bool, json: bool) {
    let report = build_report(repo_root, verbose, check_updates);

    if json {
        println!(
            "{}",
            serde_json::to_string_pretty(&report).unwrap_or_else(|_| "{}".to_string())
        );
    } else {
        print_report(&report);
    }

    if report.errors > 0 {
        std::process::exit(1);
    }
}

#[expect(clippy::too_many_lines)]
fn build_report(repo_root: &Path, verbose: bool, check_updates: bool) -> HealthReport {
    let health_cfg = HealthConfig::load(repo_root);

    let mut pass = 0u32;
    let mut warn = 0u32;
    let mut fail = 0u32;
    let mut tool_checks = Vec::new();
    let mut repo_checks = Vec::new();
    let mut env_checks = Vec::new();
    let mut custom_checks = Vec::new();

    let checks = default_checks();

    // ── Tool version checks ─────────────────────────────────────
    for check in &checks {
        let result = check.execute();
        match &result {
            CheckResult::Ok(version) => {
                let constraint_msg = if let Some(cfg) = &health_cfg {
                    validate_tool_version(&check.name, version, cfg)
                } else {
                    None
                };

                if let Some(msg) = constraint_msg {
                    let mut details = Vec::new();
                    append_tool_hint_details(&mut details, &check.name, health_cfg.as_ref());
                    tool_checks.push(HealthCheckRecord {
                        name: check.name.clone(),
                        status: "error".into(),
                        summary: format!("{version} ({msg})"),
                        details,
                    });
                    fail += 1;
                } else {
                    let update_msg = if check_updates {
                        check_for_update(&check.name, version, health_cfg.as_ref())
                    } else {
                        None
                    };

                    if let Some(latest) = update_msg {
                        tool_checks.push(HealthCheckRecord {
                            name: check.name.clone(),
                            status: "warning".into(),
                            summary: format!("{version} (update available: {latest})"),
                            details: Vec::new(),
                        });
                        warn += 1;
                    } else {
                        tool_checks.push(HealthCheckRecord {
                            name: check.name.clone(),
                            status: "ok".into(),
                            summary: version.clone(),
                            details: Vec::new(),
                        });
                        pass += 1;
                    }
                }
            }
            CheckResult::Missing => {
                let required = health_cfg
                    .as_ref()
                    .and_then(|cfg| cfg.tools.get(&check.name))
                    .is_some_and(|t| t.required);

                if required {
                    let mut details = Vec::new();
                    append_tool_hint_details(&mut details, &check.name, health_cfg.as_ref());
                    tool_checks.push(HealthCheckRecord {
                        name: check.name.clone(),
                        status: "error".into(),
                        summary: "required but not found".into(),
                        details,
                    });
                    fail += 1;
                } else if verbose {
                    tool_checks.push(HealthCheckRecord {
                        name: check.name.clone(),
                        status: "info".into(),
                        summary: "not found".into(),
                        details: Vec::new(),
                    });
                }
            }
            CheckResult::Error(msg) => {
                tool_checks.push(HealthCheckRecord {
                    name: check.name.clone(),
                    status: "error".into(),
                    summary: msg.clone(),
                    details: Vec::new(),
                });
                fail += 1;
            }
        }
    }

    // Check for tools in config that aren't in the default list.
    if let Some(cfg) = &health_cfg {
        let known_names: Vec<&str> = checks.iter().map(|c| c.name.as_str()).collect();
        for (name, req) in &cfg.tools {
            if known_names.contains(&name.as_str()) {
                continue;
            }
            // Custom tool from config — probe it.
            let cmd = req.command.as_deref().unwrap_or(name.as_str());
            let args: Vec<&str> = req
                .version_args
                .as_ref()
                .map_or_else(|| vec!["--version"], |a| {
                    a.iter().map(String::as_str).collect()
                });

            let custom_check = Check::tool(cmd, &args, parse_first_line).display_name(name);
            let result = custom_check.execute();

            match &result {
                CheckResult::Ok(version) => {
                    let constraint_msg = validate_tool_version(name, version, cfg);
                    if let Some(msg) = constraint_msg {
                        let mut details = Vec::new();
                        append_tool_hint_details(&mut details, name, Some(cfg));
                        tool_checks.push(HealthCheckRecord {
                            name: name.clone(),
                            status: "error".into(),
                            summary: format!("{version} ({msg})"),
                            details,
                        });
                        fail += 1;
                    } else {
                        let update_msg = if check_updates {
                            check_for_update(name, version, Some(cfg))
                        } else {
                            None
                        };

                        if let Some(latest) = update_msg {
                            tool_checks.push(HealthCheckRecord {
                                name: name.clone(),
                                status: "warning".into(),
                                summary: format!("{version} (update available: {latest})"),
                                details: Vec::new(),
                            });
                            warn += 1;
                        } else {
                            tool_checks.push(HealthCheckRecord {
                                name: name.clone(),
                                status: "ok".into(),
                                summary: version.clone(),
                                details: Vec::new(),
                            });
                            pass += 1;
                        }
                    }
                }
                CheckResult::Missing if req.required => {
                    let mut details = Vec::new();
                    append_tool_hint_details(&mut details, name, Some(cfg));
                    tool_checks.push(HealthCheckRecord {
                        name: name.clone(),
                        status: "error".into(),
                        summary: "required but not found".into(),
                        details,
                    });
                    fail += 1;
                }
                CheckResult::Missing => {
                    if verbose {
                        tool_checks.push(HealthCheckRecord {
                            name: name.clone(),
                            status: "info".into(),
                            summary: "not found".into(),
                            details: Vec::new(),
                        });
                    }
                }
                CheckResult::Error(msg) => {
                    tool_checks.push(HealthCheckRecord {
                        name: name.clone(),
                        status: "error".into(),
                        summary: msg.clone(),
                        details: Vec::new(),
                    });
                    fail += 1;
                }
            }
        }
    }

    // ── Repo-specific checks ────────────────────────────────────
    if let Some(branch) = run_cmd("git", &["branch", "--show-current"]) {
        repo_checks.push(HealthCheckRecord {
            name: "branch".into(),
            status: "ok".into(),
            summary: branch,
            details: Vec::new(),
        });
        pass += 1;
    } else {
        repo_checks.push(HealthCheckRecord {
            name: "branch".into(),
            status: "error".into(),
            summary: "not a git repository".into(),
            details: Vec::new(),
        });
        fail += 1;
    }

    let config_path = repo_root.join(".repo").join("config.toml");
    if config_path.is_file() {
        repo_checks.push(HealthCheckRecord {
            name: "config".into(),
            status: "ok".into(),
            summary: ".repo/config.toml".into(),
            details: Vec::new(),
        });
        pass += 1;
    } else {
        repo_checks.push(HealthCheckRecord {
            name: "config".into(),
            status: "warning".into(),
            summary: "no .repo/config.toml".into(),
            details: Vec::new(),
        });
        warn += 1;
    }

    let health_path = repo_root.join(".repo").join("health.toml");
    if health_path.is_file() {
        repo_checks.push(HealthCheckRecord {
            name: "health".into(),
            status: "ok".into(),
            summary: ".repo/health.toml".into(),
            details: Vec::new(),
        });
        pass += 1;
    } else {
        repo_checks.push(HealthCheckRecord {
            name: "health".into(),
            status: "info".into(),
            summary: "no .repo/health.toml (run `repo health init`)".into(),
            details: Vec::new(),
        });
    }

    let docs_root = repo_root.join("_docs");
    if docs_root.is_dir() {
        let subdirs: Vec<&str> = ["plans", "designs", "adrs", "references"]
            .iter()
            .filter(|d| docs_root.join(d).is_dir())
            .copied()
            .collect();
        repo_checks.push(HealthCheckRecord {
            name: "_docs".into(),
            status: "ok".into(),
            summary: subdirs.join(", "),
            details: Vec::new(),
        });
        pass += 1;
    } else {
        repo_checks.push(HealthCheckRecord {
            name: "_docs".into(),
            status: "warning".into(),
            summary: "not found".into(),
            details: Vec::new(),
        });
        warn += 1;
    }

    check_virtualenv(&mut env_checks, &mut pass, &mut warn);

    if let Some(toolchain) = run_cmd("rustup", &["show", "active-toolchain"]) {
        let short = toolchain.split_whitespace().next().unwrap_or(&toolchain);
        repo_checks.push(HealthCheckRecord {
            name: "toolchain".into(),
            status: "ok".into(),
            summary: short.into(),
            details: Vec::new(),
        });
        pass += 1;
    }

    // ── Environment / cage detection ────────────────────────────
    let cage = detect_cage();
    let cage_str = cage_name(&cage);

    // Validate cage against config.
    let cage_ok = if let Some(cfg) = &health_cfg {
        if cfg.environment.allowed_runtimes.is_empty() {
            true
        } else {
            cfg.environment
                .allowed_runtimes
                .iter()
                .any(|r| r == cage_str)
        }
    } else {
        true
    };

    if cage_ok {
        let label = match cage {
            Cage::Host => "bare host".to_string(),
            _ => cage_str.to_string(),
        };
        env_checks.push(HealthCheckRecord {
            name: "runtime".into(),
            status: "ok".into(),
            summary: label,
            details: Vec::new(),
        });
        pass += 1;
    } else {
        env_checks.push(HealthCheckRecord {
            name: "runtime".into(),
            status: "error".into(),
            summary: format!(
                "{cage_str} (allowed: {})",
                health_cfg
                    .as_ref()
                    .expect("checked above")
                    .environment
                    .allowed_runtimes
                    .join(", ")
            ),
            details: Vec::new(),
        });
        fail += 1;
    }

    // Privilege escalation.
    let privilege = detect_privilege();
    if let Some(cfg) = &health_cfg {
        let expected = &cfg.environment.privilege;
        if expected != "auto" && expected != &privilege {
            env_checks.push(HealthCheckRecord {
                name: "privilege".into(),
                status: "error".into(),
                summary: format!("found {privilege}, expected {expected}"),
                details: Vec::new(),
            });
            fail += 1;
        } else {
            env_checks.push(HealthCheckRecord {
                name: "privilege".into(),
                status: "ok".into(),
                summary: privilege,
                details: Vec::new(),
            });
            pass += 1;
        }
    } else {
        env_checks.push(HealthCheckRecord {
            name: "privilege".into(),
            status: "ok".into(),
            summary: privilege,
            details: Vec::new(),
        });
        pass += 1;
    }

    // Available shells.
    let shells = detect_shells();
    if shells.is_empty() {
        env_checks.push(HealthCheckRecord {
            name: "shells".into(),
            status: "warning".into(),
            summary: "none found in /etc/shells".into(),
            details: Vec::new(),
        });
    } else {
        // Validate required shell.
        let shell_ok = if let Some(cfg) = &health_cfg {
            if let Some(ref required) = cfg.environment.required_shell {
                shells.iter().any(|s| s == required)
            } else {
                true
            }
        } else {
            true
        };

        if shell_ok {
            env_checks.push(HealthCheckRecord {
                name: "shells".into(),
                status: "ok".into(),
                summary: shells.join(", "),
                details: Vec::new(),
            });
            pass += 1;
        } else {
            let required = &health_cfg
                .as_ref()
                .unwrap()
                .environment
                .required_shell
                .as_ref()
                .unwrap();
            env_checks.push(HealthCheckRecord {
                name: "shells".into(),
                status: "error".into(),
                summary: format!("required shell '{required}' not in: {}", shells.join(", ")),
                details: Vec::new(),
            });
            fail += 1;
        }
    }

    if let Ok(shell) = std::env::var("SHELL") {
        let short = Path::new(&shell)
            .file_name()
            .unwrap_or_default()
            .to_string_lossy()
            .into_owned();
        env_checks.push(HealthCheckRecord {
            name: "default".into(),
            status: "ok".into(),
            summary: short,
            details: Vec::new(),
        });
    }

    // ── Custom checks ────────────────────────────────────────────
    if let Some(cfg) = &health_cfg
        && !cfg.checks.is_empty()
    {
        for (name, check) in &cfg.checks {
            let output = Command::new("sh").arg("-c").arg(&check.command).output();

            match output {
                Ok(out) if out.status.success() => {
                    custom_checks.push(HealthCheckRecord {
                        name: name.clone(),
                        status: "ok".into(),
                        summary: check.description.clone(),
                        details: Vec::new(),
                    });
                    pass += 1;
                }
                Ok(out) => {
                    let is_warn = check.severity == "warning";

                    let stderr = String::from_utf8_lossy(&out.stderr);
                    let stdout = String::from_utf8_lossy(&out.stdout);
                    let detail = if !stderr.trim().is_empty() {
                        stderr.trim().lines().next().unwrap_or("")
                    } else if !stdout.trim().is_empty() {
                        stdout.trim().lines().next().unwrap_or("")
                    } else {
                        ""
                    };
                    let mut details = Vec::new();
                    if !detail.is_empty() {
                        details.push(detail.to_string());
                    }
                    if let Some(ref hint) = check.hint {
                        details.push(format!("hint: {hint}"));
                    }
                    custom_checks.push(HealthCheckRecord {
                        name: name.clone(),
                        status: if is_warn { "warning" } else { "error" }.into(),
                        summary: check.description.clone(),
                        details,
                    });

                    if is_warn {
                        warn += 1;
                    } else {
                        fail += 1;
                    }
                }
                Err(e) => {
                    custom_checks.push(HealthCheckRecord {
                        name: name.clone(),
                        status: "error".into(),
                        summary: format!("failed to run: {e}"),
                        details: Vec::new(),
                    });
                    fail += 1;
                }
            }
        }
    }

    let mut sections = vec![
        HealthSection {
            name: "Tools".into(),
            checks: tool_checks,
        },
        HealthSection {
            name: "Repository".into(),
            checks: repo_checks,
        },
        HealthSection {
            name: "Environment".into(),
            checks: env_checks,
        },
    ];
    if !custom_checks.is_empty() {
        sections.push(HealthSection {
            name: "Checks".into(),
            checks: custom_checks,
        });
    }

    HealthReport {
        passed: pass,
        warnings: warn,
        errors: fail,
        sections,
    }
}

