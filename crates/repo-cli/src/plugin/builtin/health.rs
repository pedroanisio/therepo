// ── Update checking ─────────────────────────────────────────────────

fn check_for_update(name: &str, current_raw: &str, cfg: Option<&HealthConfig>) -> Option<String> {
    let latest = health_config::check_latest_version(name, cfg)?;
    let current = extract_version_number(current_raw);

    if current.is_empty() || latest.is_empty() {
        return None;
    }

    if compare_versions(&current, &latest) == std::cmp::Ordering::Less {
        Some(format!("{current} -> {latest}"))
    } else {
        None
    }
}

// ── Install / URL hints ─────────────────────────────────────────────

fn append_tool_hint_details(details: &mut Vec<String>, name: &str, cfg: Option<&HealthConfig>) {
    let req = cfg.and_then(|c| c.tools.get(name));

    if let Some(install) = req.and_then(|r| r.install.as_deref()) {
        details.push(format!("install: {install}"));
    }

    if let Some(url) = req.and_then(|r| r.url.as_deref()) {
        details.push(format!("url: {url}"));
    }
}

// ── Version validation ──────────────────────────────────────────────

fn validate_tool_version(name: &str, raw_version: &str, cfg: &HealthConfig) -> Option<String> {
    let req = cfg.tools.get(name)?;

    let version = extract_version_number(raw_version);

    if let Some(ref exact) = req.exact_version
        && version != *exact
    {
        return Some(format!("expected {exact}, got {version}"));
    }

    if let Some(ref min) = req.min_version
        && compare_versions(&version, min) == std::cmp::Ordering::Less
    {
        return Some(format!("minimum {min}, got {version}"));
    }

    None
}

/// Compare dot-separated version strings numerically.
/// "1.70.0" vs "1.9.0" -> Greater (70 > 9).
fn compare_versions(a: &str, b: &str) -> std::cmp::Ordering {
    let parse = |s: &str| -> Vec<u64> {
        s.split('.')
            .map(|p| p.parse::<u64>().unwrap_or(0))
            .collect()
    };
    let va = parse(a);
    let vb = parse(b);
    let len = va.len().max(vb.len());
    for i in 0..len {
        let a = va.get(i).copied().unwrap_or(0);
        let b = vb.get(i).copied().unwrap_or(0);
        let ord = a.cmp(&b);
        if ord != std::cmp::Ordering::Equal {
            return ord;
        }
    }
    std::cmp::Ordering::Equal
}

/// Extract a semver-like version number from raw output.
fn extract_version_number(raw: &str) -> String {
    for word in raw.split_whitespace() {
        let word = word.strip_prefix('v').unwrap_or(word);
        if word.chars().next().is_some_and(|c| c.is_ascii_digit()) && word.contains('.') {
            let clean: String = word
                .chars()
                .take_while(|c| c.is_ascii_digit() || *c == '.')
                .collect();
            if clean.contains('.') {
                return clean;
            }
        }
    }
    String::new()
}

